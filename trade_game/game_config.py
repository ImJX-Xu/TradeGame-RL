"""
游戏通用变量配置管理文件

此文件统一管理所有游戏通用变量（不包括CSV存储的硬数据）。
修改此文件即可调整游戏平衡性参数。

注意：商品数据（products.csv）、城市数据（cities.csv）、路线数据（routes.csv）
不在本文件中管理，请直接修改对应的CSV文件。
"""

# ============================================================================
# 一、运输成本相关
# ============================================================================
#
# 出行费用公式：
#   陆运费用 = 路线里程(km) × LAND_COST_PER_KM
#   海运费用 = 路线里程(km) × SEA_COST_PER_KM
#   若起点或终点为台北/高雄，再叠加 TAIWAN_CUSTOMS（每趟固定额）
# 快速出行时，上述「费用」会再乘以 FAST_TRAVEL_COST_MULTIPLIER。
# ----------------------------------------------------------------------------

# 陆运成本：每公里费用（元/km）
# 包含：油费、过路费等。费用 = 里程 × 本参数。
LAND_COST_PER_KM: float = 0.15

# 海运成本：每公里费用（元/km）
# 包含：油费、港口杂费等。费用 = 里程 × 本参数。
SEA_COST_PER_KM: float = 0.6

# 台湾航线关税：大陆↔台湾航线额外费用（元/趟）
# 仅当起点或终点为台北/高雄时触发，每趟行程加收此固定金额。
TAIWAN_CUSTOMS: float = 50.0

# 海运载重乘数：海运不限制载重；费用 = 基础费用 × 载重乘数
# 载重乘数 = 1 + 当前货物单位数/总载重（总载重由货车数量决定，见 capacity_utils.total_storage_capacity）
# 总载重为 0 时按 1 计（不乘）

# 台湾商品采购关税：在台北/高雄采购商品时的额外费用比例
# 基于商品「基础采购价」的百分比。公式：关税额 = 基础采购价 × 本参数。
# 例如 0.10 表示 10%，采购价 = 基础价×(1+λ) + 基础价×0.10。
TAIWAN_PURCHASE_TARIFF_RATE: float = 0.10


# ============================================================================
# 二、快速出行相关
# ============================================================================
#
# 快速出行与普通出行共用同一路线里程，仅费用与耗时不同：
#   快速出行费用 = 普通出行费用 × FAST_TRAVEL_COST_MULTIPLIER
#   快速出行天数 = max(FAST_TRAVEL_MIN_DAYS, floor(普通出行天数 / FAST_TRAVEL_TIME_DIVISOR))
# ----------------------------------------------------------------------------

# 快速出行价格倍数：快速出行费用 = 普通出行费用 × 本参数
FAST_TRAVEL_COST_MULTIPLIER: float = 3.0

# 快速出行时间除数：快速出行天数 = 普通出行天数 ÷ 本参数（向下取整）
# 再与 FAST_TRAVEL_MIN_DAYS 取大，保证至少 1 天。
FAST_TRAVEL_TIME_DIVISOR: float = 3

# 快速出行最少天数：快速出行天数不得低于本值
FAST_TRAVEL_MIN_DAYS: int = 0


# ============================================================================
# 三、运输速度相关
# ============================================================================
#
# 普通出行耗时公式：
#   出行天数 = ceil(路线里程 / 对应速度)
# 陆运用 LAND_SPEED_KM_PER_DAY，海运用 SEA_SPEED_KM_PER_DAY。
# ----------------------------------------------------------------------------

# 陆运速度：每天可行驶的公里数（km/天），用于计算陆路出行天数
LAND_SPEED_KM_PER_DAY: float = 400.0

# 海运速度：每天可行驶的公里数（km/天），用于计算海运出行天数
SEA_SPEED_KM_PER_DAY: float = 250.0


# ============================================================================
# 四、车辆相关
# ============================================================================
#
# 耐久度：每次陆运出行后，每辆车耐久减少 = 本次里程 × TRUCK_DURABILITY_LOSS_PER_KM × 100（百分比）
# 维修：费用 = TRUCK_REPAIR_COST_BASE × 维修百分比(0～100) × 车辆数；耗时 TRUCK_REPAIR_DAYS 天
# 出行条件：任意一辆车耐久 >= TRUCK_MIN_DURABILITY_FOR_TRAVEL 才允许陆运
# 人力：每日扣除 = (车辆数 - 1) × DAILY_LABOR_PER_TRUCK（首辆不计）
# ----------------------------------------------------------------------------

# 初始车辆数量：游戏开始时玩家拥有的货车数量
INITIAL_TRUCK_COUNT: int = 1

# 初始车辆总载重：游戏开始时所有货车的总载重（单位），用于计算可载货量
INITIAL_TRUCK_CAPACITY: int = 100

# 初始车辆耐久度：游戏开始时每辆车的耐久度（0～100）
INITIAL_TRUCK_DURABILITY: float = 100.0

# 车辆耐久损耗率：每公里损耗的耐久度（线性）
# 公式：单次出行后每车耐久减少 = 本次里程(km) × 本参数 × 100（百分比点）
# 例如 0.001 表示每 1 km 损耗 0.1%，1000 km 损耗 100%
TRUCK_DURABILITY_LOSS_PER_KM: float = 0.001

# 车辆维修费用基数：维修总费用 = 本参数 × 维修百分比(0～100) × 车辆数
# 例如损耗 50%、2 辆车：100 × 50 × 2 = 10,000 元
TRUCK_REPAIR_COST_BASE: float = 100.0

# 车辆维修时间：一次维修操作消耗的游戏天数（维修期间不可出行）
TRUCK_REPAIR_DAYS: int = 7

# 车辆最低耐久度限制：任意一辆车耐久度低于本值时禁止陆运出行
TRUCK_MIN_DURABILITY_FOR_TRAVEL: float = 30.0

# 购车单价：在车厂每购买一辆货车的价格（元）
TRUCK_PURCHASE_PRICE: float = 40_000.0

# 每车载重：每新增一辆车增加的货车总载重（单位），与 INITIAL_TRUCK_CAPACITY 一致时即单车 100 单位
TRUCK_CAPACITY_PER_VEHICLE: int = 100

# 每日人力成本：除第一辆车外，每多一辆车每天扣除的本金（元/天）
# 公式：每日人力 = max(0, 车辆数 - 1) × 本参数
DAILY_LABOR_PER_TRUCK: float = 50.0


# ============================================================================
# 五、船只相关
# ============================================================================
#
# 船只通过「租船」获得，不直接购买。总载重 = 船只数 × SHIP_CAPACITY_EACH。
# 耐久度在租用时为 INITIAL_SHIP_DURABILITY，具体损耗逻辑见租船/海运模块。
# ----------------------------------------------------------------------------

# 初始船只数量：游戏开始时玩家已租用的船只数（通常为 0，需在港口租船）
INITIAL_SHIP_COUNT: int = 0

# 单艘船只载重：每艘船的载重容量（单位），总载重 = 船只数 × 本参数
SHIP_CAPACITY_EACH: int = 200

# 船只初始耐久度：每次新租船时，该船的初始耐久度（0～100）
INITIAL_SHIP_DURABILITY: float = 100.0


# ============================================================================
# 六、借贷相关
# ============================================================================
#
# 利息：每日结算一次，每笔未还本金的当日利息 = 该笔本金 × LOAN_DAILY_INTEREST_RATE
# 可借额度：最大可借金额 = 预估资产 × LOAN_MULTIPLIER（具体以 loans 模块为准）
# ----------------------------------------------------------------------------

# 日利率：每笔贷款每日产生的利息 = 该笔当前本金 × 本参数
# 例如 0.001 表示 0.1%/天，10000 元本金每天产生 10 元利息
LOAN_DAILY_INTEREST_RATE: float = 0.001

# 借贷倍数：可借额度与预估资产的倍数关系（1.0 表示最多借约 1 倍资产）
# 实际最大可借金额由 loans 模块按「预估资产 × 本参数」等规则计算
LOAN_MULTIPLIER: float = 1.0


# ============================================================================
# 七、价格乘数相关
# ============================================================================
#
# 售卖价公式（economy.sell_unit_price）：
#   本地售卖：售卖价 = 基础采购价 × (1+λ)
#   异地售卖：售卖价 = 基础采购价×(1+异地利润率)×(1+λ) × K高消
# 其中 K高消：高消费城市 = HIGH_CONSUMPTION_CITY_MULTIPLIER，其他城市 = 1.0
# 高消费城市名单见 data.HIGH_CONSUMPTION_CITIES（北京、上海、广州、深圳、台北、高雄）
# 异地售卖时 K异 与里程正相关：在全局最小里程处为 REMOTE_SALE_MULTIPLIER_MIN，
# 在全局最大里程处为 REMOTE_SALE_MULTIPLIER_MAX，中间线性插值；本地售卖 K异=1。
# 高消城 K高消 = HIGH_CONSUMPTION_CITY_MULTIPLIER。
# ----------------------------------------------------------------------------

# 异地售卖乘数 K异 范围（与里程正相关）：K异 = MIN + (km - km_min)/(km_max - km_min) * (MAX - MIN)，限制在 [MIN, MAX]
# 里程取「产地到售卖城市」的最短路线（陆/海取可通行方式中的较小值）；多产地取最远距离
REMOTE_SALE_MULTIPLIER_MIN: float = 1.0
REMOTE_SALE_MULTIPLIER_MAX: float = 1.5

# 高消费城市乘数：在高消费城市售卖时的最终价格乘数 K高消
# 公式：异地售卖价 = ...×(1+λ)× 本参数（高消费城）；非高消费城为 1.0
HIGH_CONSUMPTION_CITY_MULTIPLIER: float = 1.2


# ============================================================================
# 八、运输损耗相关
# ============================================================================
#
# 运输损耗在 inventory 模块计算。各类商品「损耗乘数」公式：
#
# 1) 易损（如电子）：mult = 1 + km / LOSS_REF_KM
#    里程越大损耗乘数越大，有效损耗率 = 商品基础损耗率 × mult。
#
# 2) 生鲜：mult = (1 + days / LOSS_REF_DAYS) × (1 + S×(days/10)^2)
#    前半部分为「时间参考」线性项，后半为保质期老化项（S 见 inventory.PERISHABLE_AGING_STRENGTH）。
#
# 3) 普通（民生/轻工）：mult = 1 + (km / LOSS_REF_KM) / 3
#    与易损同结构但强度约为其 1/3。
#
# 实际损耗量 = 理论损耗 × 随机因子，随机因子在 [MIN, MAX] 内，避免完全确定性。
# 注意：inventory 中若未引用本文件，则实际使用的为 inventory 内同名常量，修改本文件后需同步。
# ----------------------------------------------------------------------------

# 易损商品损耗参考里程（km）：易损类损耗乘数 = 1 + 运输里程 / 本参数
LOSS_REF_KM: float = 2000.0

# 生鲜商品损耗参考天数（天）：生鲜类「基础时间乘数」= 1 + 运输天数 / 本参数
LOSS_REF_DAYS: float = 5.0

# 运输损耗随机因子下界：单次运输实际损耗 = 理论损耗 × rand(本参数, TRANSPORT_LOSS_RANDOM_FACTOR_MAX)
TRANSPORT_LOSS_RANDOM_FACTOR_MIN: float = 0.9

# 运输损耗随机因子上界：实际损耗在理论值的 90%～110% 之间波动
TRANSPORT_LOSS_RANDOM_FACTOR_MAX: float = 1.1


# ============================================================================
# 九、初始游戏状态
# ============================================================================
#
# 新游戏或重置时的默认状态：现金、所在地、日期。车辆/船只由第四节、第五节给出。
# ----------------------------------------------------------------------------

# 初始资金：新游戏开始时玩家的现金（元）
INITIAL_CASH: float = 1000.0

# 初始位置：新游戏开始时玩家所在城市（需在 cities 中存在）
INITIAL_LOCATION: str = "郑州"

# 初始天数：新游戏开始时的游戏内日期（第几天）
INITIAL_DAY: int = 1


# ============================================================================
# 十、价格波动相关（λ 参数调整系数）
# ============================================================================
#
# 每日价格由「城市×商品」的 λ 驱动，每日凌晨刷新（economy.refresh_daily_lambdas）。
#
# --- 核心公式 ---
# 1) 每日 λ 更新（一阶自回归 + 高斯噪声，再截断到区间内）：
#
#      λ_new = α' × λ_prev + ε,   ε ~ N(0, σ')
#      然后 λ_new 截断到 [λ_min', λ_max']
#
#    其中（每城市每商品）：
#      α' = 商品.lambda_alpha × LAMBDA_ALPHA_ADJUSTMENT
#      σ' = 商品.lambda_sigma × LAMBDA_SIGMA_ADJUSTMENT
#      λ_min'、λ_max' 默认取商品自身的 lambda_min、lambda_max；
#      若该城市属于高消费城市，则再乘以下方三个「高消费城市」系数。
#
# 2) 高消费城市（北京、上海、广州、深圳、台北、高雄）单独放大波动：
#      α'_高消 = α' × HIGH_CITY_LAMBDA_ALPHA_MULTIPLIER   （惯性变小，更易波动）
#      σ'_高消 = σ' × HIGH_CITY_LAMBDA_SIGMA_MULTIPLIER   （噪声变大）
#      λ_min'_高消 = λ_min × HIGH_CITY_LAMBDA_RANGE_MULTIPLIER
#      λ_max'_高消 = λ_max × HIGH_CITY_LAMBDA_RANGE_MULTIPLIER
#
# 3) 价格使用 λ：
#      采购价（产地）= 基础采购价 × (1 + λ)
#      售卖价（异地）= 基础采购价×(1+异地利润率)×(1+λ) × K高消（见第七节）
#
# --- 参数作用简述 ---
#  LAMBDA_ALPHA_ADJUSTMENT：全局缩小惯性，使 λ 更易偏离昨日值（<1 更随机）。
#  LAMBDA_SIGMA_ADJUSTMENT：全局放大每日随机扰动，价格波动幅度更大。
#  HIGH_CITY_LAMBDA_RANGE_MULTIPLIER：高消费城市 λ 允许范围更宽（区间×本值）。
#  HIGH_CITY_LAMBDA_ALPHA_MULTIPLIER：高消费城市惯性再乘本值（<1 更易波动）。
#  HIGH_CITY_LAMBDA_SIGMA_MULTIPLIER：高消费城市噪声再乘本值（>1 波动更大）。
#
# 注意：economy 模块若尚未引用本文件，则实际使用的是其内部硬编码，需改为从本文件读取以生效。
# ----------------------------------------------------------------------------

# λ 惯性系数调整：每日 λ 的惯性 α' = 商品.lambda_alpha × 本参数
# 越小则「昨日 λ」权重越低，当日价格越随机（建议 <1，如 0.6）
LAMBDA_ALPHA_ADJUSTMENT: float = 0.6

# λ 扰动标准差调整：每日随机项 ε 的标准差 σ' = 商品.lambda_sigma × 本参数
# 越大则单日价格波动幅度越大（建议 >1，如 2.0）
LAMBDA_SIGMA_ADJUSTMENT: float = 2.0

# 高消费城市 λ 区间倍数：高消费城市的 [λ_min, λ_max] 均乘以本参数，区间更宽
HIGH_CITY_LAMBDA_RANGE_MULTIPLIER: float = 1.5

# 高消费城市 λ 惯性倍数：高消费城市的 α' 再乘以本参数（<1 表示惯性更小、更易波动）
HIGH_CITY_LAMBDA_ALPHA_MULTIPLIER: float = 0.9

# 高消费城市 λ 扰动倍数：高消费城市的 σ' 再乘以本参数（>1 表示噪声更大）
HIGH_CITY_LAMBDA_SIGMA_MULTIPLIER: float = 1.3


